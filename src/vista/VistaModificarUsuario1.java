public void guardarUsuario() throws Exception {
    Usuario usuario = new Usuario();

    String tipo = ComboTipo.getSelectedItem().toString().trim();
    String nombre = txtNombre.getText().trim();
    String apellidos = txtApellidos.getText().trim();
    String contraseña = txtContraseña.getText().trim();
    String correo = txtCorreo.getText().trim();
    String identificacion = txtIdentificacion.getText().trim();

    if (txtUsuario.getText().equals("") || nombre.equals("") || apellidos.equals("") || contraseña.equals("") || correo.equals("") || identificacion.equals("")) {
        JOptionPane.showMessageDialog(null, "Complete todos los campos");

        txtUsuario.setBackground(Color.RED);
        txtNombre.setBackground(Color.RED);
        txtApellidos.setBackground(Color.RED);
        txtContraseña.setBackground(Color.RED);
        txtCorreo.setBackground(Color.RED);
        txtIdentificacion.setBackground(Color.RED);

    } else if (!validador.validacionCorreo(correo)) {
        //JOptionPane.showMessageDialog(null, "Correo electrónico no válido");
        label1.setVisible(true);
        txtCorreo.setBackground(Color.RED);
    } else if (!validador.validacionContraseña(contraseña)) {
        JOptionPane.showMessageDialog(null, "Contraseña no válida. Debe contener 8 caracteres con al menos una mayúscula, una minúscula, un número y un carácter especial.");
        txtContraseña.setBackground(Color.RED);
    } else if (identificacion.length() != 8 || !identificacion.matches("\\d+")) {
        //JOptionPane.showMessageDialog(null, "La identificación debe tener 8 caracteres y contener solo números");
        label2.setVisible(true);
        txtIdentificacion.setBackground(Color.RED);
    } else {
        txtIdentificacion.setBackground(Color.WHITE);

        int identi = Integer.parseInt(identificacion);

        if (!ControladorUsuario.existeUsuario(correo)) {
            // Set user data
            usuario.setUsuario(txtUsuario.getText().trim());
            usuario.setTipo(tipo);
            usuario.setNombre(nombre);
            usuario.setApellidos(apellidos);
            usuario.setContraseña(contraseña);
            usuario.setCorreo(correo);
            usuario.setIdentificacion(identi);

            if (ControladorUsuario.insertarUsuario(usuario)) {
                JOptionPane.showMessageDialog(null, "Usuario guardado correctamente");
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar el usuario");
                 
            }
        } else {
            //JOptionPane.showMessageDialog(null, "Ya existe un usuario con ese correo electrónico");
            //jLabel4.setVisible(true);
        }
    }
}



