public void ModificarUsuario() {
    String usuario = txtUsuario.getText();
    String tipo = (String) ComboTipo.getSelectedItem();
    String nombre = txtNombre.getText();
    String apellidos = txtApellidos.getText();
    String contraseña = new String(txtContraseña.getPassword()).trim();
    String correo = txtCorreo.getText();
  
    String identificacion = txtIdentificacion.getText().trim();  
         int identi = Integer.parseInt(identificacion);   

    // Validar campos obligatorios
    if (usuario.isEmpty() || tipo == null || nombre.isEmpty() || apellidos.isEmpty() || contraseña.isEmpty() || correo.isEmpty() || identificacion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Mostrar cuadro de diálogo de confirmación
    int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea realizar estos cambios?", "Confirmar Cambios", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    int selectedRow = tablaUsuarios.getSelectedRow();
    boolean actualizado = false;

    try {
        if (selectedRow >= 0) {
            // Obtener el correo del usuario seleccionado
            String correoSeleccionado = tablaUsuarios.getValueAt(selectedRow, 5).toString();

            // Obtener el objeto Usuario a partir del correo
            Usuario usuarioExistente = ControladorUsuario.getUsuario(correoSeleccionado);
            
            if (usuarioExistente != null) {
                // Actualizar los datos del usuario
                usuarioExistente.setUsuario(usuario);
                usuarioExistente.setTipo(tipo);
                usuarioExistente.setNombre(nombre);
                usuarioExistente.setApellidos(apellidos);
                usuarioExistente.setContraseña(contraseña);
                usuarioExistente.setCorreo(correo);
                usuarioExistente.setIdentificacion(identi);

                actualizado = ControladorUsuario.actualizarUsuario(usuarioExistente);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo encontrar el usuario para actualizar.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else {
            // Crear un nuevo usuario si no hay una fila seleccionada
            Usuario nuevoUsuario = new Usuario(usuario, tipo, nombre, apellidos, contraseña, correo, identi);
            actualizado = ControladorUsuario.insertarUsuario(nuevoUsuario);
        }

        if (actualizado) {
            cargarDatos();
            Limpiar();
            JOptionPane.showMessageDialog(this, "Datos guardados correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar los datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error inesperado: " + e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();  // Añadir para ver detalles del error en la consola
    }
}















public void cargarDatos() throws Exception {
    
    List<Usuario> listaUsuarios = ControladorUsuario.obtenerUsuario();
    DefaultTableModel modeloTabla = new DefaultTableModel(
    new String[]{"Usuario", "Tipo", "Nombre", "Apellidos", "Contraseña","Correo", "Identificacion"}, 0);

    modeloTabla.setRowCount(0);
       
        for (Usuario usuario : listaUsuarios) {
            modeloTabla.addRow (new  Object[]{
                usuario.getUsuario(),
                usuario.getTipo(),
                usuario.getNombre(),
                usuario.getApellidos(),
                usuario.getContraseña(),
                usuario.getCorreo(),
                 usuario.getIdentificacion(),
               
            });
        }
         tablaUsuarios.setModel(modeloTabla);
    }




public void eliminarUsuario() throws Exception {
    int selectedRow = tablaUsuarios.getSelectedRow();
    if (selectedRow >= 0) {
        // Obtener el correo del usuario seleccionado en la tabla
        String correoSeleccionado = tablaUsuarios.getValueAt(selectedRow, 5).toString();

        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de que deseas eliminar este usuario?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            boolean eliminado = ControladorUsuario.eliminarUsuario(correoSeleccionado);
            if (eliminado) {
                cargarDatos(); // Recargar los datos de la tabla
                Limpiar(); // Limpiar los campos del formulario
                JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un usuario de la tabla para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
}




public void cargarDatosss() throws Exception {
    List<Usuario> listaUsuario = ControladorUsuario.obtenerUsuario();
    DefaultTableModel model = (DefaultTableModel) tablaUsuarios.getModel();
    model.setRowCount(0);

    for (Usuario usuario : listaUsuario) {
        Object[] row = new Object[7];
        row[0] = usuario.getUsuario();
        row[1] = usuario.getTipo();
        row[2] = usuario.getNombre();
        row[3] = usuario.getApellidos();
        row[4] = usuario.getContraseña();
        row[5] = usuario.getCorreo();
        row[6] = usuario.getIdentificacion();
        
        model.addRow(row);
    }
}


public void rellenarDatos() {
    int selectedRow = tablaUsuarios.getSelectedRow();
    if (selectedRow >= 0) {
        txtUsuario.setText(tablaUsuarios.getValueAt(selectedRow, 0).toString());
        ComboTipo.setSelectedItem(tablaUsuarios.getValueAt(selectedRow, 1).toString());
        txtNombre.setText(tablaUsuarios.getValueAt(selectedRow, 2).toString());
        txtApellidos.setText(tablaUsuarios.getValueAt(selectedRow, 3).toString());
        txtContraseña.setText(tablaUsuarios.getValueAt(selectedRow, 4).toString()); // Maneja esto con cuidado
        txtCorreo.setText(tablaUsuarios.getValueAt(selectedRow, 5).toString());
        txtIdentificacion.setText(tablaUsuarios.getValueAt(selectedRow, 6).toString());
    }
}









