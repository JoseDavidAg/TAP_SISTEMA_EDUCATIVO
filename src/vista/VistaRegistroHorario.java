    private void cargarGrupos() {
    Connection cn = null;
    Statement statement = null;
    ResultSet resultSet = null;

    try {
       
        cn = Conexion.conectar();
        statement = cn.createStatement();
        resultSet = statement.executeQuery("SELECT * FROM grupo");

        comboGrupo.removeAllItems();

        while (resultSet.next()) {
            String IdGrupo = resultSet.getString("IdGrupo");
            int IdCurso = resultSet.getInt("IdCurso");
            comboGrupo.addItem(new Grupo(IdGrupo, IdCurso).toString());
        }
        //System.out.println("Grupos cargados exitosamente");
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
     
        try {
            if (resultSet != null) resultSet.close();
            if (statement != null) statement.close();
            if (cn != null) cn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}
    


    
    public void cargarMaterias() {
        Connection cn = null;
        Statement statement = null;
        ResultSet resultSet = null;

        try {
            cn = Conexion.conectar();
            statement = cn.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM materia");

            // Elimina todos los elementos de los JComboBox
            for (JComboBox<String> comboBox : comboBoxesM) {
                comboBox.removeAllItems();
            }

            while (resultSet.next()) {
                String IdMateria = resultSet.getString("IdMateria");
                
                int IdCurso = resultSet.getInt("IdCurso");
                String materia = new Materia(IdMateria, IdCurso).toString();

                // Agrega la materia a cada JComboBox
                for (JComboBox<String> comboBox : comboBoxesM) {
                    comboBox.addItem(materia);
                }
            }
            //System.out.println("Materias cargadas exitosamente");
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (resultSet != null) resultSet.close();
                if (statement != null) statement.close();
                if (cn != null) cn.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    
    
    
    
    
